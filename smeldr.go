// Default timeout values applied by New when the caller does not set them
// in Config. These are conservative production defaults.
const (
	defaultReadTimeout  = 5 * time.Second
	defaultWriteTimeout = 10 * time.Second
	defaultIdleTimeout  = 120 * time.Second
)

// Config holds the application-wide configuration passed to [New].
//
// BaseURL and Secret are required; all other fields are optional.
//
// Timeouts default to 5 s (read), 10 s (write), and 120 s (idle) when left as
// zero. Set them explicitly to override.
type Config struct {
	// BaseURL is the canonical URL of the site, e.g. "https://example.com"
	// (no trailing slash). Required.
	BaseURL string

	// Secret is the HMAC signing key used by [BearerHMAC], [CookieSession], and
	// [SignToken]. It must be at least 16 bytes. Required.
	//
	// When Auth is nil, Secret is used to validate Bearer tokens automatically
	// via [BearerHMAC]. Set [Config.Auth] to override.
	Secret []byte

	// Auth is the [AuthFunc] used to authenticate requests. When nil, Forge
	// defaults to [BearerHMAC] using [Config.Secret]. Set this explicitly to
	// use [CookieSession], [AnyAuth], or a custom [AuthFunc].
	//
	// Example — cookie sessions instead of bearer tokens:
	//
	//	Auth: smeldr.CookieSession("session", secret)
	//
	// Example — both bearer tokens and cookie sessions:
	//
	//	Auth: smeldr.AnyAuth(
	//	    smeldr.BearerHMAC(secret),
	//	    smeldr.CookieSession("session", secret),
	//	)
	Auth AuthFunc

	// TokenStore enables database-backed named bearer token management. When
	// set, [VerifyBearerToken] checks the smeldr_tokens table to validate that
	// a token has not been revoked. The smeldr_tokens table must exist before
	// the application starts; see [TokenStore] for the required DDL.
	// Optional — leave nil to use HMAC-only token validation.
	TokenStore *TokenStore

	// Version is the application version string. It is an optional field for
	// application authors who want to track their own release version; forge
	// itself does not use this field in any built-in endpoint.
	Version string

	// DB is the database connection used by content modules.
	// It accepts *sql.DB, *sql.Tx, or any value that satisfies [DB].
	// Optional — leave nil to use in-memory repositories only.
	DB DB

	// HTTPS forces an HTTP→HTTPS redirect for all plain-HTTP requests when
	// true. The App handler checks r.TLS and the X-Forwarded-Proto header so
	// this works correctly behind a reverse proxy. Optional.
	HTTPS bool

	// ReadTimeout is the maximum time to read the full request, including the
	// body. Defaults to 5 s. Optional.
	ReadTimeout time.Duration

	// WriteTimeout is the maximum time to write the full response. Defaults to
	// 10 s. Optional.
	WriteTimeout time.Duration

	// IdleTimeout is the maximum keep-alive idle time between requests.
	// Defaults to 120 s. Optional.
	IdleTimeout time.Duration

	// PreviewTokenExpiry sets how long a preview token generated by
	// [App.GeneratePreviewToken] remains valid. Defaults to 12 hours when zero.
	// Optional.
	PreviewTokenExpiry time.Duration

	// NavMode selects how the application's navigation tree is populated.
	// Use [NavModeDB] to persist navigation items in the smeldr_nav database
	// table (requires [Config.DB]; panics at startup if DB is nil).
	// Use [NavModeCode] to supply items directly via [App.Nav].
	// The zero value disables navigation entirely. Optional.
	NavMode NavMode

	// AppSchema sets the app-level JSON-LD structured data block emitted on
	// every page as a <script type="application/ld+json"> element. When set
	// here, it is applied automatically at startup; an explicit [App.SEO] call
	// with an [AppSchema] takes precedence. This field is also populated when
	// a smeldr.config file contains the org_name or org_type keys. Optional.
	AppSchema *AppSchema

	// OGDefaults sets the app-level Open Graph and Twitter Card fallback values
	// applied to every page. When set here, it is applied automatically at
	// startup; an explicit [App.SEO] call with an [OGDefaults] takes
	// precedence. Also populated from smeldr.config og_image and twitter_site
	// keys. Optional.
	OGDefaults *OGDefaults

	// MediaPath is the filesystem directory where forge-media stores uploaded
	// files. Defaults to "./media" when zero. Optional — only read by
	// forge-media; ignored by forge core.
	MediaPath string

	// MediaMaxSize is the maximum permitted upload size in bytes. Defaults to
	// 5242880 (5 MB) when zero. Optional — only read by forge-media; ignored
	// by forge core.
	MediaMaxSize int64

	// MediaUploadTokenExpiry sets how long an upload token generated by
	// [App.GenerateUploadToken] remains valid. Defaults to 15 minutes when
	// zero. Optional — only read by forge-media; ignored by forge core.
	MediaUploadTokenExpiry time.Duration

	// Dev enables development mode. When true, [App.Static] serves files from
	// disk (devDir) instead of the embedded FS, so changes are visible without
	// rebuilding. Set from os.Getenv("DEV") == "1" or via the smeldr.config key
	// "dev = true". Optional.
	Dev bool

	// WebhookWorkers is the number of concurrent goroutines that poll and
	// deliver outbound webhook jobs. Defaults to 10 when zero. Optional —
	// only used when [App.Webhooks] is called.
	WebhookWorkers int
}

// MustConfig validates cfg and returns it unchanged.
//
// Panics with a descriptive message if:
//   - Config.BaseURL is empty or not a valid absolute URL
//   - Config.Secret is fewer than 16 bytes
//
// MustConfig also loads a smeldr.config file from the working directory (or the
// path set in the SMELDR_CONFIG environment variable) and merges its key/value
// pairs into cfg. Go-code field values always take precedence over file values.
// Supported smeldr.config keys: base_url, https, nav_mode, org_name, org_type,
// twitter_site, og_image, media_path, media_max_size, dev.
// The key "secret" is explicitly forbidden in smeldr.config and causes a panic.
//
// Typical usage:
//
//	app := smeldr.New(smeldr.MustConfig(smeldr.Config{
//	    BaseURL: os.Getenv("BASE_URL"),
//	    Secret:  []byte(os.Getenv("SECRET")),
//	}))
func MustConfig(cfg Config) Config {
	// D30: load smeldr.config (or the path in SMELDR_CONFIG) and merge file
	// values into cfg. Go-code fields always take precedence.
	if path := os.Getenv("SMELDR_CONFIG"); path != "" {
		fc, err := loadConfigFile(path)
		if err != nil {
			panic("smeldr: " + err.Error())
		}
		cfg = mergeFileConfig(cfg, fc)
	} else {
		fc, _ := loadConfigFile("smeldr.config") // missing file is not an error
		cfg = mergeFileConfig(cfg, fc)
	}
	if cfg.BaseURL == "" {
		panic(`smeldr: Config.BaseURL is required (e.g. "https://example.com")`)
	}
	u, err := url.ParseRequestURI(cfg.BaseURL)
	if err != nil || !u.IsAbs() {
		panic(fmt.Sprintf("smeldr: Config.BaseURL %q is not a valid absolute URL: %v", cfg.BaseURL, err))
	}
	if len(cfg.Secret) < 16 {
		panic("smeldr: Config.Secret must be at least 16 bytes")
	}
	return cfg
}

// Registrator is implemented by any value that can register its HTTP routes on
// a [http.ServeMux]. [*Module] satisfies this interface automatically.
//
// Pass a pre-built [*Module] to [App.Content] to register it:
//
//	posts := smeldr.NewModule[*Post](&Post{}, smeldr.Repo(repo))
//	app.Content(posts)
type Registrator interface {
	Register(mux *http.ServeMux)
}

// SEOOption is implemented by any value that modifies the app-level SEO
// configuration. Pass SEOOption values to [App.SEO]:
//
//	app.SEO(&smeldr.RobotsConfig{AIScraper: smeldr.AskFirst, Sitemaps: true})
type SEOOption interface {
	applySEO(*seoState)
}

