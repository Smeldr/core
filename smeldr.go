// httpsRedirect returns a middleware that redirects plain-HTTP requests to
// their HTTPS equivalents with a 301 Moved Permanently response.
//
// The redirect is skipped when the request is already over TLS (r.TLS != nil)
// or when the X-Forwarded-Proto header equals "https" (reverse-proxy scenario).
//
// GET /_health is always exempt so that reverse-proxy health checks (e.g.
// Caddy health_uri) receive a 200 response without being redirected
// (Amendment A59).
func httpsRedirect() func(http.Handler) http.Handler {
	return func(next http.Handler) http.Handler {
		return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
			if r.URL.Path == "/_health" {
				next.ServeHTTP(w, r)
				return
			}
			if r.TLS != nil || r.Header.Get("X-Forwarded-Proto") == "https" {
				next.ServeHTTP(w, r)
				return
			}
			target := "https://" + r.Host + r.RequestURI
			http.Redirect(w, r, target, http.StatusMovedPermanently)
		})
	}
}

// Handler returns the composed http.Handler that serves all registered routes
// behind the global middleware stack.
//
// When Config.HTTPS is true, an HTTP→HTTPS redirect middleware is prepended
// before all user-supplied middleware.
//
// Handler is called automatically by [App.Run]. Call it directly when you need
// to hand the handler to your own server (e.g. for testing or embedding):
//
//	srv := &http.Server{Handler: app.Handler()}
func (a *App) Handler() http.Handler {
	if a.sitemapStore != nil && !a.sitemapIndexRegistered {
		a.sitemapIndexRegistered = true
		a.mux.Handle("GET /sitemap.xml", a.sitemapStore.IndexHandler(a.cfg.BaseURL))
	}
	if a.seo.robots != nil && !a.robotsTxtRegistered {
		a.robotsTxtRegistered = true
		a.mux.Handle("GET /robots.txt", RobotsTxtHandler(*a.seo.robots, a.cfg.BaseURL))
	}
	if a.llmsStore != nil && a.llmsStore.HasCompact() && !a.llmsTxtRegistered {
		a.llmsTxtRegistered = true
		a.mux.Handle("GET /llms.txt", a.llmsStore.CompactHandler())
	}
	if a.llmsStore != nil && a.llmsStore.HasFull() && !a.llmsFullTxtRegistered {
		a.llmsFullTxtRegistered = true
		a.mux.Handle("GET /llms-full.txt", a.llmsStore.FullHandler())
	}
	if a.feedStore != nil && a.feedStore.HasFeeds() && !a.feedIndexRegistered {
		a.feedIndexRegistered = true
		a.mux.Handle("GET /feed.xml", a.feedStore.IndexHandler())
	}
	if len(a.cookieDecls) > 0 && !a.cookieManifestReg {
		a.cookieManifestReg = true
		u, _ := url.Parse(a.cfg.BaseURL)
		a.mux.Handle("GET /.well-known/cookies.json",
			newCookieManifestHandler(u.Hostname(), a.cookieDecls, a.cookieManifestOpts...),
		)
	}
	if !a.redirectManifestReg {
		a.redirectManifestReg = true
		u2, _ := url.Parse(a.cfg.BaseURL)
		a.mux.Handle("GET /.well-known/redirects.json",
			newRedirectManifestHandler(u2.Hostname(), a.redirectStore, a.redirectManifestOpts...),
		)
	}
	// Dynamic content: load runtime-defined types from DB and register routes.
	// Must be done before the standalone dispatch block so the 2-segment
	// wildcard and /_content/ routes are registered first.
	if a.dynamicContentEnabled {
		a.loadDynamicTypes(context.Background())
	}
	if a.dynamicContentEnabled && !a.dynamicContentReg {
		a.dynamicContentReg = true
		dynAuth := a.cfg.Auth
		if dynAuth == nil {
			dynAuth = BearerHMAC(string(a.cfg.Secret))
		}
		a.mux.Handle("POST /_content/types", newDefineTypeHandler(a, dynAuth))
		a.mux.Handle("GET /_content/{type}", newAdminListHandler(a, dynAuth))
		a.mux.Handle("GET /_content/{type}/{id}", newAdminGetHandler(a, dynAuth))
		a.mux.Handle("POST /_content/{type}", newCreateContentHandler(a, dynAuth))
		a.mux.Handle("PATCH /_content/{type}/{id}", newUpdateContentHandler(a, dynAuth))
		a.mux.Handle("POST /_content/{type}/{id}/status", newSetStatusHandler(a, dynAuth))
	}
	// Single-segment dispatch: GET /{slug} routes to dynamic list, standalone
	// modules, or the redirect store (404 fallback). Always registered so that
	// the redirect store is reached via a consistent path regardless of whether
	// standalone modules are present.
	//
	// GET /{slug}/aidoc is only registered when standalone modules exist; the
	// 2-segment literal-wildcard pattern conflicts with per-module patterns
	// like GET /posts/{slug} registered by non-standalone modules.
	if !a.standaloneReg {
		a.standaloneReg = true
		sds := a.standaloneModules
		rs := a.redirectStore
		a.mux.Handle("GET /{slug}", http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
			slug := r.PathValue("slug")
			for _, sd := range sds {
				if sd.findAndServe(w, r, slug) {
					return
				}
			}
			rs.handler().ServeHTTP(w, r)
		}))
		if len(sds) > 0 {
			a.mux.Handle("GET /{slug}/aidoc", http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
				slug := r.PathValue("slug")
				for _, sd := range sds {
					if sd.findAndServeAIDoc(w, r, slug) {
						return
					}
				}
				WriteError(w, r, ErrNotFound)
			}))
		}
	}
	if !a.redirectFallbackReg {
		a.redirectFallbackReg = true
		a.mux.Handle("/", a.redirectStore.handler())
	}
	// D30: auto-apply Config.AppSchema and Config.OGDefaults loaded from
	// smeldr.config. App.SEO() calls take precedence (applied only when nil).
	if a.cfg.AppSchema != nil && a.seo.appSchema == nil {
		a.seo.appSchema = a.cfg.AppSchema
	}
	if a.cfg.OGDefaults != nil && a.seo.ogDefaults == nil {
		og := a.cfg.OGDefaults
		// Resolve root-relative og_image path against BaseURL so scrapers
		// receive an absolute URL (e.g. "/static/og.png" → "https://example.com/static/og.png").
		if strings.HasPrefix(og.Image.URL, "/") && a.cfg.BaseURL != "" {
			resolved := &OGDefaults{
				TwitterSite:    og.TwitterSite,
				TwitterCreator: og.TwitterCreator,
				Image: Image{
					URL:    strings.TrimRight(a.cfg.BaseURL, "/") + og.Image.URL,
					Width:  og.Image.Width,
					Height: og.Image.Height,
				},
			}
			a.seo.ogDefaults = resolved
		} else {
			a.seo.ogDefaults = og
		}
	}
	if len(a.templateModules) > 0 {
		bindErrorTemplates(a.templateModules)
		for _, tp := range a.templateModules {
			if s, ok := tp.(interface {
				setSEODefaults(*OGDefaults, *AppSchema, *HeadAssets)
			}); ok {
				s.setSEODefaults(a.seo.ogDefaults, a.seo.appSchema, a.seo.headAssets)
			}
		}
	}
	if a.auditStore != nil && !a.auditHandlerReg {
		a.auditHandlerReg = true
		auditAuth := a.cfg.Auth
		if auditAuth == nil {
			auditAuth = BearerHMAC(string(a.cfg.Secret))
		}
		a.mux.Handle("GET /_audit", newAuditHandler(auditAuth, a.auditStore))
	}
	if a.logRing != nil && !a.logsHandlerReg {
		a.logsHandlerReg = true
		logsAuth := a.cfg.Auth
		if logsAuth == nil {
			logsAuth = BearerHMAC(string(a.cfg.Secret))
		}
		a.mux.Handle("GET /_logs", newLogsHandler(logsAuth, a.logRing))
	}
	// A66: probe smeldr_tokens table at startup when a TokenStore is configured.
	// A83: auto-create a bootstrap admin token when smeldr_tokens is empty.
	if a.tokenStore != nil {
		if err := a.tokenStore.probeTable(context.Background()); err != nil {
			fmt.Fprintf(os.Stderr, "WARN  forge: TokenStore is configured but the smeldr_tokens table is missing or inaccessible — create it using the DDL in the TokenStore documentation\n")
		} else {
			a.tokenStore.ensureBootstrap(context.Background())
		}
	}
	// D29: initialise the navigation tree when NavMode is set or App.Nav() items
	// were supplied. NavModeDB migrates and loads from smeldr_nav; NavModeCode
	// builds the tree from the items supplied via App.Nav().
	if a.cfg.NavMode == NavModeDB {
		if a.cfg.DB == nil {
			panic("smeldr: Config.NavMode is NavModeDB but Config.DB is nil — set Config.DB or use NavModeCode")
		}
		u, _ := url.Parse(a.cfg.BaseURL)
		navCtx := NewBackgroundContext(u.Hostname())
		a.navTree = &NavTree{}
		if err := a.navTree.migrate(navCtx, a.cfg.DB); err != nil {
			fmt.Fprintf(os.Stderr, "WARN  forge: NavTree migration failed: %v\n", err)
			a.navTree = nil
		} else if err := a.navTree.load(navCtx, a.cfg.DB); err != nil {
			fmt.Fprintf(os.Stderr, "WARN  forge: NavTree load failed: %v\n", err)
			a.navTree = nil
		}
	} else if len(a.navCodeItems) > 0 {
		a.navTree = &NavTree{}
		a.navTree.setCode(a.navCodeItems)
	}
	if a.navTree != nil {
		for _, m := range a.navTreeModules {
			m.setNavTree(a.navTree)
		}
	}
	// A34: trigger a one-shot startup rebuild of all derived content (sitemap,
	// feed, AI index) so that items already in the repository at server start
	// (seed data, pre-loaded fixtures) are reflected before the first request.
	if !a.rebuildDone && len(a.rebuilderModules) > 0 {
		a.rebuildDone = true
		u, _ := url.Parse(a.cfg.BaseURL)
		ctx := NewBackgroundContext(u.Hostname())
		modules := a.rebuilderModules
		go func() {
			for _, rb := range modules {
				rb.rebuildAll(ctx)
			}
		}()
	}
	mws := a.middleware
	if a.cfg.HTTPS {
		mws = append([]func(http.Handler) http.Handler{httpsRedirect()}, mws...)
	}
	return Chain(a.mux, mws...)
}

