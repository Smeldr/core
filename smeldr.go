// ServeDynamicContent activates the dynamic content substrate. It creates the
// necessary database tables (smeldr_content_type_schemas, smeldr_dynamic_content)
// and migrates existing tables. Handler() will then register the /_content/ admin
// namespace and, for each content type with a URLPrefix, specific public routes.
// Panics when Config.DB is nil.
//
//	smeldr.CreateSchemaTable(db)   // optional: ServeDynamicContent calls this
//	app.ServeDynamicContent()
func (a *App) ServeDynamicContent() *App {
	if a.cfg.DB == nil {
		panic("smeldr: ServeDynamicContent requires Config.DB")
	}
	if err := CreateSchemaTable(a.cfg.DB); err != nil {
		panic("smeldr: ServeDynamicContent CreateSchemaTable: " + err.Error())
	}
	if err := MigrateSchemaKindColumn(a.cfg.DB); err != nil {
		slog.Warn("smeldr: ServeDynamicContent MigrateSchemaKindColumn", "err", err)
	}
	if err := MigrateURLPrefixColumn(a.cfg.DB); err != nil {
		slog.Warn("smeldr: ServeDynamicContent MigrateURLPrefixColumn", "err", err)
	}
	if err := CreateBlockTables(a.cfg.DB); err != nil {
		panic("smeldr: ServeDynamicContent CreateBlockTables: " + err.Error())
	}
	if a.sitemapStore == nil {
		a.sitemapStore = NewSitemapStore()
	}
	a.dynamicContentEnabled = true
	return a
}

// forgeVersions reads the binary's embedded build info and returns a map of
// short module name → version for all Smeldr core and companion modules
// (smeldr.dev/core and related modules). Each module path under
// smeldr.dev/ maps to a key equal to the sub-path
// (e.g. smeldr.dev/mcp → "mcp", smeldr.dev/core → "core").
// The leading "v" is stripped from version strings ("v1.1.5" → "1.1.5").
// Returns nil when build info is unavailable or no smeldr modules are found.
func forgeVersions() map[string]string {
	const base = "smeldr.dev/"
	info, ok := debug.ReadBuildInfo()
	if !ok {
		return nil
	}
	result := make(map[string]string)
	add := func(path, version string) {
		if !strings.HasPrefix(path, base) || version == "" {
			return
		}
		v := strings.TrimPrefix(version, "v")
		key := strings.ReplaceAll(strings.TrimPrefix(path, base), "-", "_")
		result[key] = v
	}
	add(info.Main.Path, info.Main.Version)
	for _, dep := range info.Deps {
		add(dep.Path, dep.Version)
	}
	if len(result) == 0 {
		return nil
	}
	return result
}

// Health mounts GET /_health on the App's mux.
//
// The endpoint always returns HTTP 200 with Content-Type application/json.
// Framework versions are read from the binary's embedded build info and
// included in the response. The core module uses the key "core";
// companion modules use a key derived from their sub-path (e.g. "mcp").
// When build info is unavailable, only {"status":"ok"} is returned.
//
// Call Health before [App.Handler] or [App.Run]:
//
//	app.Health()
//	// GET /_health → {"status":"ok","core":"1.25.0","mcp":"1.11.2"}
func (a *App) Health() {
	versions := forgeVersions()
	// Pre-sort companion module keys for deterministic JSON output.
	var companions []string
	for k := range versions {
		if k != "core" {
			companions = append(companions, k)
		}
	}
	slices.Sort(companions)

	a.mux.Handle("GET /_health", http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)
		var b strings.Builder
		b.WriteString(`{"status":"ok"`)
		if v, ok := versions["core"]; ok {
			fmt.Fprintf(&b, `,"core":%q`, v)
		}
		for _, k := range companions {
			fmt.Fprintf(&b, `,%q:%q`, k, versions[k])
		}
		b.WriteByte('}')
		fmt.Fprint(w, b.String())
	}))
}

// Run starts the HTTP server on addr (e.g. ":8080") and blocks until
// SIGINT or SIGTERM is received.
//
// On receiving a signal, Run initiates a graceful shutdown with a 5-second
// deadline, waits for active connections to drain, and returns nil.
// Non-shutdown errors from ListenAndServe are returned directly.
//
//	if err := app.Run(":8080"); err != nil {
//	    log.Fatal(err)
//	}
func (a *App) Run(addr string) error {
	// Load shared partials once, then push to each module before parsing.
	partials, err := loadPartials(a.partialsDir)
	if err != nil {
		return err
	}
	if len(partials) > 0 {
		for _, tp := range a.templateModules {
			if sp, ok := tp.(interface{ setPartials([]string) }); ok {
				sp.setPartials(partials)
			}
		}
	}

	// Parse HTML templates before starting the server. Fail fast so startup
	// errors are obvious rather than surfacing as 406 responses at request time.
	for _, tp := range a.templateModules {
		if err := tp.parseTemplates(); err != nil {
			return err
		}
	}

	// Log framework versions at startup.
	if versions := forgeVersions(); len(versions) > 0 {
		var parts []string
		if v, ok := versions["core"]; ok {
			parts = append(parts, "core "+v)
		}
		companions := make([]string, 0, len(versions))
		for k := range versions {
			if k != "core" {
				companions = append(companions, k)
			}
		}
		slices.Sort(companions)
		for _, k := range companions {
			parts = append(parts, k+" "+versions[k])
		}
		fmt.Fprintf(os.Stderr, "smeldr: %s\n", strings.Join(parts, ", "))
	}

	srv := &http.Server{
		Addr:         addr,
		Handler:      a.Handler(),
		ReadTimeout:  a.cfg.ReadTimeout,
		WriteTimeout: a.cfg.WriteTimeout,
		IdleTimeout:  a.cfg.IdleTimeout,
	}

	// serveErr receives the result of ListenAndServe.
	serveErr := make(chan error, 1)

	// Inject webhook delivery hooks and signal listeners into all modules.
	a.wireSignalBus()

	// Start the webhook worker pool if configured.
	poolCtx, poolCancel := context.WithCancel(context.Background())
	defer poolCancel()
	if a.webhookPool != nil {
		a.webhookPool.Start(poolCtx)
		defer a.webhookPool.Stop()
	}

	// Start the scheduled-publishing ticker if any modules were registered.
	if len(a.schedulerModules) > 0 {
		u, _ := url.Parse(a.cfg.BaseURL)
		bgCtx := NewBackgroundContext(u.Hostname())
		sched := newScheduler(a.schedulerModules, bgCtx)
		schedCtx, schedCancel := context.WithCancel(context.Background())
		sched.Start(schedCtx)
		defer func() {
			schedCancel()
			sched.Wait()
		}()
	}

	go func() {
		serveErr <- srv.ListenAndServe()
	}()

	// Block until an OS signal or a fatal server error.
	ctx, stop := signal.NotifyContext(context.Background(), os.Interrupt, syscall.SIGTERM)
	defer stop()

	select {
	case err := <-serveErr:
		// Server failed before any signal — return the error directly.
		if errors.Is(err, http.ErrServerClosed) {
			return nil
		}
		return err
	case <-ctx.Done():
		// Signal received — begin graceful shutdown.
		stop()
	}

	shutdownCtx, cancel := context.WithTimeout(context.Background(), 5*time.Second)
	defer cancel()

	if err := srv.Shutdown(shutdownCtx); err != nil {
		return err
	}
	for _, sp := range a.stoppableModules {
		sp.Stop()
	}
	return nil
}
