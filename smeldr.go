// Config returns a copy of the application configuration. It is intended for
// use by companion packages (such as forge-media) that need access to
// configuration fields — [Config.BaseURL], [Config.MediaPath],
// [Config.MediaMaxSize] — without the host application repeating those values
// at the call site. The returned value is a copy and cannot be used to mutate
// the running configuration.
func (a *App) Config() Config { return a.cfg }

// Secret returns the HMAC signing secret from the application configuration.
// It is intended for use by forge-mcp and other companion packages that must
// verify tokens minted with [SignToken] but cannot access [Config] directly.
func (a *App) Secret() []byte { return a.cfg.Secret }

// BaseURL returns the canonical base URL of the application (Config.BaseURL),
// without a trailing slash. It is intended for use by forge-mcp and other
// companion packages that build absolute URLs.
func (a *App) BaseURL() string { return strings.TrimRight(a.cfg.BaseURL, "/") }

// GeneratePreviewToken returns a signed preview token for the given module
// prefix and content slug. The token is valid for [Config.PreviewTokenExpiry]
// (default 12 h). Pass it to the module's show endpoint via the ?preview=
// query parameter to bypass the Published-only visibility guard for draft content.
//
// Example — build a full preview URL:
//
//	token := app.GeneratePreviewToken("posts", "my-draft")
//	url := app.BaseURL() + "/posts/my-draft?preview=" + token
//
// The token binds both prefix and slug so that a token for /posts/foo cannot
// be replayed on /docs/foo.
func (a *App) GeneratePreviewToken(prefix, slug string) string {
	ttl := a.cfg.PreviewTokenExpiry
	if ttl == 0 {
		ttl = 12 * time.Hour
	}
	return encodePreviewToken(prefix, slug, a.cfg.Secret, ttl)
}

// GenerateUploadToken returns a short-lived signed token that authorises a
// single POST /media upload request without a full bearer token. The token is
// valid for [Config.MediaUploadTokenExpiry] (default 15 m). Pass it in the
// Authorization header as "UploadToken <token>".
//
// Example — generate a token and hand it to an HTTP client:
//
//	token := app.GenerateUploadToken()
//	uploadURL := app.BaseURL() + "/media"
//
// The token carries no slug or prefix binding; it authorises any upload within
// the TTL. forge-media restricts UploadToken uploads to image MIME types only.
func (a *App) GenerateUploadToken() string {
	ttl := a.cfg.MediaUploadTokenExpiry
	if ttl == 0 {
		ttl = 15 * time.Minute
	}
	return encodeUploadToken(a.cfg.Secret, ttl)
}

// ValidateUploadToken verifies a token produced by [GenerateUploadToken].
// Returns nil when the token is valid and unexpired, or [ErrUnauth] otherwise.
// Intended for use by forge-media's upload handler.
func (a *App) ValidateUploadToken(token string) error {
	return decodeUploadToken(token, a.cfg.Secret)
}

