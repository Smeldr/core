// Webhooks wires an outbound webhook delivery engine into the application.
// Call this after [App.Content] has registered all modules and before
// [App.Run]:
//
//	store := smeldr.NewWebhookStore(db, secret)
//	app.Webhooks(store)
//
// At [App.Run] time, Forge injects the signal bus into every registered
// content module. After each lifecycle signal (create, update, publish,
// schedule, archive, delete), the bus dispatches to all registered handlers,
// including the webhook delivery handler registered here. The handler looks up
// active subscriptions from the store, builds the event payload, and enqueues
// an [OutboundJob] for the worker pool to deliver.
//
// The worker pool starts when [App.Run] is called and stops on graceful
// shutdown. The number of workers defaults to 10; set [Config.WebhookWorkers]
// to override.
//
// Webhooks requires [Config.DB] to be set — the same DB is shared with the
// store and the outbound job tables.
func (a *App) Webhooks(store *WebhookStore) *App {
	a.webhookStore = store
	workers := a.cfg.WebhookWorkers
	if workers == 0 {
		workers = 10
	}
	pool := newWorkerPool(a.cfg.DB, store, realClock{}, workers)
	a.webhookPool = pool

	// Register webhook delivery as signal bus subscribers so that every
	// content lifecycle event enqueues delivery jobs.
	for _, sig := range []Signal{
		AfterCreate, AfterUpdate, AfterPublish, AfterUnpublish,
		AfterSchedule, AfterArchive, AfterDelete,
	} {
		s := sig
		a.OnSignal(s, func(ctx context.Context, ev SignalEvent) error {
			return webhookDispatch(ctx, ev, s, store, pool)
		})
	}
	return a
}

// Audit wires store as the audit trail for this application. It subscribes to
// [AfterPublish], [AfterSchedule], [AfterArchive], and [AfterDelete] on the
// signal bus and persists an [AuditRecord] for each event. Errors from
// [AuditStore.Append] are logged at Warn level and never propagated to the caller.
//
// Audit also mounts GET /_audit — an Editor-or-higher HTTP endpoint that
// returns a JSON array of [AuditRecord] values. Optional query parameters:
// from, to (RFC3339), type (content type name), actor (actor ID).
//
// The smeldr_audit_log table must exist before Audit is called.
// See [NewAuditStore] for the required DDL.
func (a *App) Audit(store AuditStore) *App {
	a.auditStore = store
	for _, sig := range []Signal{AfterPublish, AfterSchedule, AfterArchive, AfterDelete} {
		s := sig
		a.OnSignal(s, func(ctx context.Context, ev SignalEvent) error {
			r := AuditRecord{
				ID:            NewID(),
				Timestamp:     ev.Timestamp,
				Signal:        s,
				ContentType:   ev.Type,
				Slug:          ev.Slug,
				ActorID:       ev.ActorID,
				ActorRole:     ev.ActorRole,
				PreviousState: ev.PreviousState,
			}
			if err := store.Append(ctx, r); err != nil {
				slog.WarnContext(ctx, "smeldr: audit append failed",
					"error", err, "signal", s, "type", ev.Type, "slug", ev.Slug)
			}
			return nil
		})
	}
	return a
}

